const mongoose = require("mongoose");

const PostSchema = new mongoose.Schema({
  productname: {
    type: String,
    required: true,
    trim: true,
  },
  category: {
    type: String,
    required: true,
    trim: true,
  },
  price: {
    type: Number,
    required: true,
    trim: true,
  },
  unit: {
    type: String,
    required: true,
    trim: true,
  },
  company: {
    type: String,
    required: true,
    trim: true,
  },
});

module.exports = mongoose.model("post", PostSchema);
